function multiply(){
    let num1 = document.getElementById('num-1').value;
    let num2 = document.getElementById('num-2').value;
    document.getElementById('result').innerHTML = num1 * num2;  
}

function devide(){
    let num1 = document.getElementById('num-1').value;
    let num2 = document.getElementById('num-2').value;
    document.getElementById('result').innerHTML = num1 / num2;  
}
